.INCLUDE "m328Pdef.inc"
.org 0x0000 jmp SETUP

SETUP:
LDI R16, 0x02
OUT DDRB, R16               
LDI R16, 0b01100000
STS ADMUX, R16
LDI R16, 0b10100001
STS TCCR1A, R16
LDI R16, 0b00001001
STS TCCR1B, R16
ldi R16,0xFF
sts OCR1AH,R16
ldi R16,0x00
sts OCR1AL,R16

READ:
LDI R16, 0b11000101
STS ADCSRA, R16 
WAIT:
LDS R16,ADCSRA
SBRS R16,4 
RJMP WAIT
LDS R16, ADCH 
LDS R17, ADCL 
STS OCR1AH,R17
STS OCR1AL,R16
RJMP READ
