 .include "m328pdef.inc"
.org 0x0000 JMP SETUP
.org 0x0022 JMP SEND

SETUP:
 LDI R16,0B00010000   ;MISO E SS', START
 OUT DDRB,R16
 LDI R16,0B11010000 
 OUT SPCR,R16
 LDI R16,0X08
 OUT SPDR,R16        ;CARREGANDO 8 NO RDATA
 SEI

 WAIT:
  RJMP WAIT

  SEND:
  LDI R16,0B00010100 ; STOP
  OUT DDRB,R16
  LDI R16,0B11010000
  OUT SPCR,R16
  LDI R16,0X80
  OUT SPDR,R16
  SEI
  RETI